public class BMI_value {
    public static void main(String[] args) {
        double weight = 51;
        double height = 1.63;
        double BMI = (weight/(height * height));
        System.out.println("My weight: " + weight);
        System.out.println("My height: " + height);
        System.out.println("My BMI: " + BMI);
    }
}
